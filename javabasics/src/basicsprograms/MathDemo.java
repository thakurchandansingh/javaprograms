package basicsprograms;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println(Math.sqrt(10));
        System.out.println(Math.max(2, 4));
        System.out.println(Math.min(10, 9));
        System.out.println(Math.abs(-10));
        System.out.println(Math.abs(10));
        System.out.println(Math.pow(2, 3));
        System.out.println(Math.pow(1000, 1 / 3.0));
        System.out.println(Math.signum(0));
        System.out.println(Math.signum(10));
        System.out.println(Math.signum(-10));
        System.out.println(Math.floor(9.70));
        System.out.println(Math.ceil(9.70));
        System.out.println(Math.round(9.5));
        System.out.println(Math.round(9.49999));
        System.out.println(Math.sin(Math.PI / 2));
        System.out.println(Math.sin(Math.PI));
        System.out.println(Math.cos(0));
        System.out.println(Math.cos(Math.PI));
        System.out.println(Math.tan(1));
        System.out.println(Math.tan(90));
        System.out.println(Math.atan(1));
        System.out.println(Math.atan(90));
        System.out.println(Math.acos(1));
        System.out.println(Math.acos(90));
        System.out.println(Math.asin(1));
        System.out.println(Math.asin(1));
        System.out.println(Math.PI);

    }
}
